    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if (txtUser.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Please Type the Username");
        }
        else if (txtPass.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Please Type the Password");
        }
        else if (txtPass.getText().equals(txtConfirm.getText()) == false){
            JOptionPane.showMessageDialog(this, "Password do not match.");
        }
        else{
            try {
                
                String username = txtUser.getText();
                String confirmpass = txtConfirm.getText();
                String usertype = txtUtype.getSelectedItem().toString();
                
                
                
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/studentmanagement","root", "");
                ps = con.prepareStatement("insert into user(username, password, utype)values(?, ?, ?)");
                ps.setString(1, username);
                ps.setString(2, confirmpass);
                ps.setString(3, usertype);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "User Created............");
                txtUser.setText("");
                txtPass.setText("");
                txtConfirm.setText("");
                txtUtype.setSelectedIndex(-1);
                txtUser.requestFocus();
                        
                        
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(NewUser.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(NewUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

